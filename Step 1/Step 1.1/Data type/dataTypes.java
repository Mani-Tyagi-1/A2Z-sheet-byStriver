/*
 *  There are two type of data types in java
 * 1. Primitive datatype  -->  Basic data types in java 
 *   they are of 8 types in java
 *      1. Int --> store integer value
 *      2. Long --> store large integer value
 *      3. Char --> store character (alphabets)
 *      4. Float --> store decimal value
 *      5. double --> store larger decimal value
 *      6. short --> store integer greater than int and smaller than long 
 *      7. boolean --> store true or false
 *      8. byte --> store 2's compliment of int
 * 
 * 2. Non-primitive datatype --> they are user defined data type in java 
 *  these are :- Classes
 *               Interface   
 *               String   
 *               Array   
 */


 public class dataTypes {
 public static void main(String[] args) {
    
     int i = 10;
     long l = 100;
     char c = 'a';
     float f = 10.5f;
     double d = 10.45;
     short s = 50;
     boolean b = true;
     byte by = -20; // int integer it is 10

     System.out.println("Integer:-" + i);
     System.out.println("Long:-" + l);
     System.out.println("Char:-" + c);
     System.out.println("Float:-" + f);
     System.out.println("Double:-" + d);
     System.out.println("Short:-" + s);
     System.out.println("Boolean:-" + b);
     System.out.println("Byte:-" + by);
 }
    
 }